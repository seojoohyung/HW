package kr.or.ddit.membership.vo;

public class MembershipVO {
	
	private String membershipCode;
	private String membershipNm;
	private String membershipDetail;

	
	
	
	public String getMembershipCode() {
		return membershipCode;
	}
	public void setMembershipCode(String membershipCode) {
		this.membershipCode = membershipCode;
	}
	public String getMembershipNm() {
		return membershipNm;
	}
	public void setMembershipNm(String membershipNm) {
		this.membershipNm = membershipNm;
	}
	public String getMembershipDetail() {
		return membershipDetail;
	}
	public void setMembershipDetail(String membershipDetail) {
		this.membershipDetail = membershipDetail;
	}
	
	
	
	
}
