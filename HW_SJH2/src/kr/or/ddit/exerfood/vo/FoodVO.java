package kr.or.ddit.exerfood.vo;

public class FoodVO {
	private int foodNum;
	private String foodName;
	private String foodContent;
	private int foodKcal;
	private int foodFat;
	private int foodProtein;
	private int foodRice;
	private int foodSweet;
	public int getFoodNum() {
		return foodNum;
	}
	public void setFoodNum(int foodNum) {
		this.foodNum = foodNum;
	}
	public String getFoodName() {
		return foodName;
	}
	public void setFoodName(String foodName) {
		this.foodName = foodName;
	}
	public String getFoodContent() {
		return foodContent;
	}
	public void setFoodContent(String foodContent) {
		this.foodContent = foodContent;
	}
	public int getFoodKcal() {
		return foodKcal;
	}
	public void setFoodKcal(int foodKcal) {
		this.foodKcal = foodKcal;
	}
	public int getFoodFat() {
		return foodFat;
	}
	public void setFoodFat(int foodFat) {
		this.foodFat = foodFat;
	}
	public int getFoodProtein() {
		return foodProtein;
	}
	public void setFoodProtein(int foodProtein) {
		this.foodProtein = foodProtein;
	}
	public int getFoodRice() {
		return foodRice;
	}
	public void setFoodRice(int foodRice) {
		this.foodRice = foodRice;
	}
	public int getFoodSweet() {
		return foodSweet;
	}
	public void setFoodSweet(int foodSweet) {
		this.foodSweet = foodSweet;
	}
	
	
}
