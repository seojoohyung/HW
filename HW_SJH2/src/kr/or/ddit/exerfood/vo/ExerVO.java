package kr.or.ddit.exerfood.vo;

public class ExerVO {
	private int exeNum;
	private String exeName;
	private String exeContent;
	public int getExeNum() {
		return exeNum;
	}
	public void setExeNum(int exeNum) {
		this.exeNum = exeNum;
	}
	public String getExeName() {
		return exeName;
	}
	public void setExeName(String exeName) {
		this.exeName = exeName;
	}
	public String getExeContent() {
		return exeContent;
	}
	public void setExeContent(String exeContent) {
		this.exeContent = exeContent;
	}
	
	
}
